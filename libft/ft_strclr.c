#include <stdlib.h>

void	ft_strclr(char *s)
{
	int i;

	i = 0;
	if (s == NULL)
		return ;
	s[i] = '\0';
	i++;
	while (s[i] != '\0')
	{
		s[i] = '\0';
		i++;
	}
}
